public class KinectCalibration
{
    public class Intrinsics
    {
        public float cx;
        public float cy;
        public float fx;
        public float fy;
        public float k1;
        public float k2;
        public float k3;
        public float k4;
        public float k5;
        public float k6;
        public float codx;
        public float cody;
        public float p2;
        public float p1;
        public float metricRadius;
    }

    public class Extrinsics
    {
        // 3x3 rotation matrix stored in row major order.
        public float[] rotation = new float[9];
        // Translation vector (x, y, z), in millimeters.
        public float[] translation = new float[3];
    }
}
